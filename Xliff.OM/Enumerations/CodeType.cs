namespace Localization.Xliff.OM.Core
{
    /// <summary>
    /// Defines the type values for the <see cref="CodeBase"/>.
    /// </summary>
    public enum CodeType
    {
        /// <summary>
        /// The code represents formatting.
        /// </summary>
        Formatting,

        /// <summary>
        /// The code represents an image.
        /// </summary>
        Image,

        /// <summary>
        /// The code represents a link.
        /// </summary>
        Link,

        /// <summary>
        /// The type is something other than one defined in this enumeration.
        /// </summary>
        Other,

        /// <summary>
        /// The code represents a quote.
        /// </summary>
        Quote,

        /// <summary>
        /// The code represents an user interface.
        /// </summary>
        UserInterface
    }
}
