namespace Localization.Xliff.OM.Core.XmlNames
{
    /// <summary>
    /// This class contains values used by the SubType property.
    /// </summary>
    internal static class SubTypeValues
    {
        /// <summary>
        /// The prefix for a SubType value.
        /// </summary>
        public const string XliffPrefix = "xlf";

        /// <summary>
        /// The format of an XLIFF SubType value.
        /// </summary>
        public const string XliffSubTypeXlfFormat = SubTypeValues.XliffPrefix + ":{0}";

        /// <summary>
        /// The value for an XLIFF SubType value representing bold text.
        /// </summary>
        public const string XliffSubTypeBold = "b";

        /// <summary>
        /// The value for an XLIFF SubType value representing italic text.
        /// </summary>
        public const string XliffSubTypeItalic = "i";

        /// <summary>
        /// The value for an XLIFF SubType value representing a line break.
        /// </summary>
        public const string XliffSubTypeLineBreak = "lb";

        /// <summary>
        /// The value for an XLIFF SubType value representing a page break.
        /// </summary>
        public const string XliffSubTypePageBreak = "pb";

        /// <summary>
        /// The value for an XLIFF SubType value representing underline text.
        /// </summary>
        public const string XliffSubTypeUnderline = "u";

        /// <summary>
        /// The value for an XLIFF SubType value representing variable text.
        /// </summary>
        public const string XliffSubTypeVariable = "var";
    }
}
