namespace Localization.Xliff.OM.XmlNames
{
    /// <summary>
    /// This class contains the names of namespace prefixes used within an XLIFF document.
    /// </summary>
    public static class NamespacePrefixes
    {
        /// <summary>
        /// The prefix for the change tracking module namespace.
        /// </summary>
        internal const string ChangeTrackingModule = "ctr";

        /// <summary>
        /// The prefix for the format style module namespace.
        /// </summary>
        internal const string FormatStyleModule = "fs";

        /// <summary>
        /// The prefix for the glossary module namespace.
        /// </summary>
        internal const string GlossaryModule = "gls";

        /// <summary>
        /// The prefix for the translation candidates module namespace.
        /// </summary>
        internal const string TranslationCandidatesModule = "mtc";

        /// <summary>
        /// The prefix for the metadata module namespace.
        /// </summary>
        internal const string MetadataModule = "mda";

        /// <summary>
        /// The prefix for the resource data module namespace.
        /// </summary>
        internal const string ResourceDataModule = "res";

        /// <summary>
        /// The prefix for the size and length restriction module namespace.
        /// </summary>
        internal const string SizeRestrictionModule = "slr";

        /// <summary>
        /// The prefix for the size and validation module namespace.
        /// </summary>
        internal const string ValidationModule = "val";

        /// <summary>
        /// The prefix for the xml namespace.
        /// </summary>
        public const string Xml = "xml";

        /// <summary>
        /// The prefix for the default namespace.
        /// </summary>
        internal const string XmlNamespace = "xmlns";
    }
}
