namespace Localization.Xliff.OM.Modules.ResourceData.XmlNames
{
    /// <summary>
    /// This class contains the names of Xml attributes for the resource data module.
    /// </summary>
    internal static class AttributeNames
    {
        /// <summary>
        /// The name of the Context Xml attribute.
        /// </summary>
        public const string Context = "context";

        /// <summary>
        /// The name of the HREF Xml attribute.
        /// </summary>
        public const string HRef = "href";

        /// <summary>
        /// The name of the Id Xml attribute.
        /// </summary>
        public const string Id = "id";

        /// <summary>
        /// The name of the Language Xml attribute.
        /// </summary>
        public const string Language = "lang";

        /// <summary>
        /// The name of the MimeType Xml attribute.
        /// </summary>
        public const string MimeType = "mimeType";

        /// <summary>
        /// The name of the Reference Xml attribute in abbreviated format.
        /// </summary>
        public const string ReferenceAbbreviated = "ref";
    }
}
